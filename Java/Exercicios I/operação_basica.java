public class operação_basica {
    public static void main(String[] args) {
        int n1 = 10; //definindo as variaveis
        int n2 = 3;

        int multi = n1 * n2; // fazendo o calculos
        int divisão = n1 /  n2;
        int soma = n1 + n2;
        int subtração = n1 - n2;
        int resto = n1 % n2;

        System.out.println("multiplicação: " + multi); // printando os resultados
        System.out.println("divsão: " + divisão);
        System.out.println("resto: " + resto);
        System.out.println("soma: " + soma);
        System.out.println("subtração: "  + subtração);

    }
}
