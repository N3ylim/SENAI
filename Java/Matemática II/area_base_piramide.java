public class area_base_piramide {
    public static void main(String[] args) {

        double volume = 180.0; // Volume da pirâmide 
        double altura = 10.0; // Altura da pirâmide 

        double calculo = (3 * volume) / altura; // fazendo o calculo

        System.out.printf("A área da base da pirâmide é: %.2f cm²", calculo); // printando o resultado
    }
}

