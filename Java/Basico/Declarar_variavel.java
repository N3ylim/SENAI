public class Declarar_variavel {
    public static void main(String[] args) {
        int idade = 18;
        double altura = 1.73;
        String nome = "Neylor Henrique";
        int força = 130;
        int Botina = 40;
        String farda = "G";

        System.out.println("Nome: " + nome);
        System.out.println("Idade: " + idade);
        System.out.println("Altura: " + altura);
        System.out.println("Força: " + força);
        System.out.println("Botina " + Botina);
        System.out.println("Farda: " + farda);
    }

}
